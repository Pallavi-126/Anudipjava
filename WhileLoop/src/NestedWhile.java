
public class NestedWhile
{

	public static void main(String[] args) {
		 
		    
		    	int i=1;
		    	
		    	while(i<=4)
		    	{
		    		i++;
		    		int j=0;
		    		System.out.println();
		    		System.out.println("Table of "+i);
		    		while(j<=9)
		    		{
		    			j++;
		    			System.out.println(i+"*"+j+"="+i*j);
		    			
		    		}
		            
		    	}	
	}
	
	}

